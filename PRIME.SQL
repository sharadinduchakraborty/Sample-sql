SET SERVEROUTPUT ON
DECLARE
N NUMBER;
F NUMBER;
A NUMBER;
X EXCEPTION;
BEGIN
N:= &NUMBER;
IF(N<0) THEN
RAISE X;
END IF;
F:=1;
IF(N=0 OR N=1) THEN
F:=0;
ELSE

FOR A IN 2..FLOOR(N/2)
LOOP
IF (MOD(N,A)=0) THEN
F:=0;
EXIT;
END IF;
END LOOP;
END IF;

IF(F=1) THEN

DBMS_OUTPUT.PUT_LINE('THE NUMBER IS PRIME');
ELSE
DBMS_OUTPUT.PUT_LINE('THE NUMBER IS NOT PRIME');
END IF;
EXCEPTION
WHEN X THEN
DBMS_OUTPUT.PUT_LINE('NUMBER MUST BE>0');
END;
/
