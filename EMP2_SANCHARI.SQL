SET SERVEROUTPUT  ON
DECLARE
A EMP.EMPNO%TYPE;
B EMP.EMPNO%TYPE;
SAL1 NUMBER;
SAL2 NUMBER;

BEGIN
SELECT SAL INTO SAL1 FROM EMP WHERE EMPNO='&A';
SELECT SAL INTO SAL2 FROM EMP WHERE EMPNO='&B';
IF(SAL1>SAL2)THEN
DBMS_OUTPUT.PUT_LINE('1');
ELSIF((SAL1<SAL2)THEN
DBMS_OUTPUT.PUT_LINE('-1');
ELSE
DBMS_OUTPUT.PUT_LINE('0');
END IF;
END;
/


