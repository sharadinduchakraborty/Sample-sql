SET SERVEROUTPUT ON
DECLARE
N   NUMBER;
I    NUMBER;
C   NUMBER;

X   EXCEPTION;

BEGIN
N='&P';
C:=0;
I:=2;
WHILE(I<=N/2)
LOOP
A:=MOD(N,I);
IF(A=0)THEN
C:=1;
EXIT;
END IF;
I:=I+1;
END LOOP;

IF(C=0)
DBMS_OUTPUT.PUT_LINE('PRIME NUMBER');
ELSE
DBMS_OUTPUT.PUT_LINE('NOT PRIME NUMBER');

EXCEPTION
WHEN X THEN
DBMS_OUTPUT.PUT_LINE('INVALID INPUT');
END;
/
