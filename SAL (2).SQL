SET SERVEROUTPUT ON
DECLARE
	ENO1 EMP.EMPNO%TYPE;
	ENO2 EMP.EMPNO%TYPE;
	SAL1 NUMBER;
	SAL2 NUMBER;
	FLAG NUMBER;
BEGIN
	SELECT EMPNO,SAL INTO ENO1,SAL1 FROM EMP WHERE EMPNO=&X;
	SELECT EMPNO,SAL INTO ENO2,SAL2 FROM EMP WHERE EMPNO=&Y;
	IF(SAL1>SAL2 ) THEN
		FLAG:=1;
	ELSIF(SAL1=SAL2) THEN
		FLAG:=0;
	ELSE
		FLAG:=-1;
	END IF;
		DBMS_OUTPUT.PUT_LINE('SALARY1 = ' || SAL1);
		DBMS_OUTPUT.PUT_LINE('SALARY2 = ' || SAL2);
		DBMS_OUTPUT.PUT_LINE(FLAG);
	EXCEPTION 
		WHEN NO_DATA_FOUND THEN
			DBMS_OUTPUT.PUT_LINE('INVALID EMPNO ');
			
END;
/