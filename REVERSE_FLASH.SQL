SET SERVEROUTPUT ON

DECLARE
N NUMBER;
F NUMBER;
R NUMBER;
X EXCEPTION;

BEGIN
N:= &NUMBER;
IF(N<0) THEN
RAISE X;
END IF;

R:=0;
LOOP
R:=R*10+MOD(N,10);
N:=FLOOR(N/10);
EXIT WHEN N=0;
END LOOP;
DBMS_OUTPUT.PUT_LINE('THE REVERSE OF A NUMBER:'||R);

 EXCEPTION
WHEN X THEN
DBMS_OUTPUT.PUT_LINE('NUMBER MUST BE>0');
END;

/

