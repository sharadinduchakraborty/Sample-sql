SET SERVEROUTPUT ON
DECLARE
ENO EMP.EMPNO%TYPE;
A NUMBER;
B NUMBER;
D NUMBER;

BEGIN
SELECT EMPNO,SAL INTO ENO,A FROM EMP WHERE EMPNO='&U';
SELECT EMPNO,SAL INTO ENO,B FROM EMP WHERE EMPNO='&V';



IF (A>B) THEN
D:=1;
ELSIF (A=B) THEN
D:=0;
ELSE
D:=-1;
END IF;
DBMS_OUTPUT.PUT_LINE('STATUS='||D);
EXCEPTION 

WHEN NO_DATA_FOUND THEN
DBMS_OUTPUT.PUT_LINE('INVALID EMPTNO');
END;
/