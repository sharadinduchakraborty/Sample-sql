SET SERVEROUTPUT ON
DECLARE
ENO EMP.EMPNO%TYPE;
NM EMP.ENAME%TYPE;
A NUMBER;
B NUMBER;
D NUMBER;

BEGIN
SELECT EMPNO,ENAME,SAL INTO ENO,NM,A FROM EMP WHERE EMPNO='&U';


IF (A<2000) THEN
B:=0;
ELSIF(A<=4000) THEN
B:=A*.02;
ELSE
B:=A*.04;
END IF;
DBMS_OUTPUT.PUT_LINE('EMPNO='||ENO);
DBMS_OUTPUT.PUT_LINE('ENAME='||NM);
DBMS_OUTPUT.PUT_LINE('SAL='||A);
DBMS_OUTPUT.PUT_LINE('TAX='||B);
D:=A-B;
DBMS_OUTPUT.PUT_LINE('SAL='||D);
EXCEPTION 

WHEN NO_DATA_FOUND THEN
DBMS_OUTPUT.PUT_LINE('INVALID EMPNO');
END;
/