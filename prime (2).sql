SET SERVEROUTPUT ON
DECLARE
I NUMBER;
N NUMBER;
A NUMBER;
B NUMBER;
C NUMBER;

X EXCEPTION;

BEGIN
N:='&P';
I:=2;
C:=0;
WHILE(I<=N/2)
LOOP
A:=MOD(N,I);
IF(A=0) THEN
   C:=1;
   EXIT;
END IF;
I:=I+1;
END LOOP;

IF (C=0) THEN
DBMS_OUTPUT.PUT_LINE('IT IS A PRIME NUMBER');
ELSE
DBMS_OUTPUT.PUT_LINE('IT IS NOT A PRIME NUMBER');
END IF;

EXCEPTION
WHEN X THEN
DBMS_OUTPUT.PUT_LINE('EXCEPTION OCCUR');
END;
/
