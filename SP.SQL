SET SERVEROUTPUT ON

DECLARE
ENO EMP.EMPNO%  TYPE;
NM  EMP.ENAME%   TYPE;
SL  NUMBER;
TAX  NUMBER;
NET_SAL  NUMBER;

BEGIN
SELECT EMPNO,ENAME,SAL INTO ENO,NM,SL FROM EMP WHERE EMPNO=&A;
IF(SL<=1000)THEN
TAX:=0;
ELSIF(SL<=2000)THEN
TAX:=0.02*SL;
ELSE
TAX:=0.04*SL;
ENDIF;

NET_SAL:=SAL-TAX;
DBMS_OUTPUT.PUT_LINE('EMPNO='||ENO);
DBMS_OUTPUT.PUT_LINE('ENAME='||NM);
DBMS_OUTPUT.PUT_LINE('SALARY='||SL);
DBMS_OUTPUT.PUT_LINE('TAX='||TAX);
DBMS_OUTPUT.PUT_LINE(PAY_AMOUNT='||NET_SAL);

EXCEPTION
WHEN NO_DATA _FOUND THEN
DBMS_OUTPUT.PUT_LINE('INVALID EMPNO');
END;
/

