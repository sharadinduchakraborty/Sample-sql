SET SERVEROUTPUT ON 
DECLARE
A NUMBER;
B NUMBER;
C NUMBER;
X EXCEPTION;
BEGIN
A:=&X;
B:=&Y;
IF(A=0 OR B=0)THEN
RAISE X;
END IF;
C:=A+B;
DBMS_OUTPUT.PUT_LINE('SUM= '||C);
IF(A>B)THEN
DBMS_OUTPUT.PUT_LINE('MAX= '||A);
ELSE
DBMS_OUTPUT.PUT_LINE('MAX= '||B);
END IF;
EXCEPTION
WHEN X THEN
DBMS_OUTPUT.PUT_LINE('INVALID INPUT');
END;
/