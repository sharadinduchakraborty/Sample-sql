SET SERVEROUTPUT ON
DECLARE
N NUMBER;
F NUMBER;
A NUMBER;
X EXCEPTION;
BEGIN
N:= &NUMBER;
IF(N=0 OR N=1) THEN
	F:=1;
ELSIF(N<0) THEN
	RAISE X;
ELSE

	F:=1;
	A:=1;
	WHILE A<=N
	LOOP
		F:=F*A;
		A:=A+1;
	END LOOP;
END IF;
DBMS_OUTPUT.PUT_LINE('FACTORIAL='||F);

EXCEPTION
WHEN X THEN
DBMS_OUTPUT.PUT_LINE('NUMBER MUST BE>0');
END;
/


