CREATE OR REPLACE FUNCTION SALSTATUS(I1 IN EMP.EMPNO% TYPE,I2 IN EMP.EMPNO% TYPE) RETURN NUMBER IS
DECLARE
SAL1 NUMBER;
SAL2 NUMBER;
BEGIN
SELECT SAL INTO SAL1 FROM EMP WHERE EMPNO=I1;
SELECT SAL INTO SAL2 FROM EMP WHERE EMPNO=I2;
IF(SAL1>SAL2)THEN
	RETURN(1);
ELSIF(SAL1=SAL2)THEN
	RETURN(0);
ELSE
	RETURN(-1);
END IF;
END;
