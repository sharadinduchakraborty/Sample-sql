SET SERVEROUTPUT ON

DECLARE
ENO EMP.EMPNO%TYPE;
NM EMP.ENAME%TYPE;
SL NUMBER;
TAX NUMBER;
NET NUMBER;

BEGIN

SELECT EMPNO,ENAME,SAL INTO ENO,NM,SL FROM EMP WHERE EMPNO='&X';

IF(SL<=2000)THEN
TAX=0;
ELSIF(SL<=4000)THEN
TAX:=(SL*0.02);
ELSE
TAX:=(SL*0.04);
END IF;
NET:=SL-TAX;
DBMS_OUTPUT.PUT_LINE( 'EMPNO='||ENO);
DBMS_OUTPUT.PUT_LINE( 'NAME='||NM);
DBMS_OUTPUT.PUT_LINE( 'SAL='||SL);
DBMS_OUTPUT.PUT_LINE('TAX='||TAX);
DBMS_OUTPUT.PUT_LINE( 'NET SAL='||NET);
EXCEPTION
WHEN NO_DATA_FOUND THEN
DBMS_OUTPUT.PUT_LINE('INVALID EMPNO');
END;
/


