SET SERVEROUTPUT ON

DECLARE 
ENO EMP.EMPNO%TYPE;
NM EMP.ENAME%TYPE;
SL NUMBER;
TAX NUMBER;
NET_SAL NUMBER;
X EXCEPTION;

BEGIN 
SELECT EMPNO,ENAME,SAL INTO ENO,NM,SL FROM EMP WHERE EMPNO='&Z';
IF(SL<2000) THEN
TAX:=0;
ELSIF(SL<=4000) THEN
TAX:=(2/100)*SL;
ELSIF(SL>4000) THEN
TAX:=(4/100)*SL;
END IF;

NET_SAL:=SL-TAX;
DBMS_OUTPUT.PUT_LINE('EMPNO='||ENO);
DBMS_OUTPUT.PUT_LINE('ENAME='||NM);
DBMS_OUTPUT.PUT_LINE('THE SALARY='||SL);
DBMS_OUTPUT.PUT_LINE('THE TAX='||TAX);
DBMS_OUTPUT.PUT_LINE('THE NET SALARY='||NET_SAL);

EXCEPTION
WHEN NO_DATA_FOUND THEN
DBMS_OUTPUT.PUT_LINE('INVALID EMPNO');

END;
/
