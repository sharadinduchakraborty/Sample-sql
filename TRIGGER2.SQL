CREATE OR REPLACE TRIGGER TR2
AFTER INSERT
ON TRANSACTION
FOR EACH ROW 
BEGIN 
IF (:NEW.TRANSACTION_MODE='WITHDRAW') THEN
UPDATE ACCOUNT_INFO SET  BALANCE=BALANCE-:NEW.AMOUNT WHERE ACC_NO=:NEW.ACC_NO;
ELSIF (:NEW.TRANSACTION_MODE='DEPOSIT') THEN
UPDATE ACCOUNT_INFO SET BALANCE=BALANCE+:NEW.AMOUNT WHERE ACC_NO=:NEW.ACC_NO;
END IF;
END;
/