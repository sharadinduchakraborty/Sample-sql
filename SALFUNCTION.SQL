CREATE OR REPLACE FUNCTION SALSTATUS(N1 IN EMP.EMPNO%TYPE,N2 IN EMP.EMPNO%TYPE)  RETURN NUMBER 
IS
S1 NUMBER;
S2 NUMBER;
BEGIN
SELECT SAL INTO S1 FROM EMP WHERE EMPNO=N1;
SELECT SAL INTO S2 FROM EMP WHERE EMPNO=N2;
IF(S1>S2) THEN
	RETURN(1);
ELSIF(S1=S2) THEN
	RETURN(0);
ELSE
	RETURN(-1);
END IF;
END;
/