SET SERVEROUTPUT ON
CREATE OR REPLACE PROCEDURE MANAGER_REPORT(M IN EMP.MGR%TYPE,N OUT NUMBER,S OUT NUMBER)
IS
C NUMBER;
T NUMBER;
CURSOR X IS SELECT EMPNO,ENAME,SAL FROM EMP WHERE MGR=M;
BEGIN
C:=0;
T:=0;
DBMS_OUTPUT.PUT_LINE('MGR : '||M);
DBMS_OUTPUT.PUT_LINE('EMPNO'||'		'||'ENAME'||'	'||'SAL');
FOR I IN X
LOOP
DBMS_OUTPUT.PUT_LINE(I.EMPNO||'	'||I.ENAME||'	'||I.SAL);
C:=C+1;
T:=T+I.SAL;
END LOOP;
N:=C;
S:=T;
END;
/



