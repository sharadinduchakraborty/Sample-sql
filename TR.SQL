CREATE OR REPLACE TRIGGER T2
AFTER INSERT
ON TRANSACTION_83
FOR EACH ROW 
BEGIN 
IF (:NEW.TMODE='WITHDRAWAL') THEN
UPDATE ACCOUNT_83 SET BALANCE=BALANCE-:NEW.AMMOUNT WHERE ACCT_NO=:NEW.ACCT_NO;
ELSIF (:NEW.TMODE='DEPOSIT') THEN
UPDATE ACCOUNT_83 SET BALANCE=BALANCE+:NEW.AMMOUNT WHERE ACCT_NO=:NEW.ACCT_NO;
END IF;
END;
/