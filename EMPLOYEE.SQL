SET SERVEROUTPUT ON 

DECLARE
ENO1  EMP.EMPNO % TYPE;
ENO2  EMP.EMPNO % TYPE;
NM1 EMP.ENAME % TYPE;
NM2 EMP.ENAME % TYPE;
SL1 NUMBER;
SL2 NUMBER;
R NUMBER;

BEGIN
SELECT EMPNO,ENAME,SAL INTO ENO1,NM1,SL1 FROM EMP WHERE EMPNO='&X';
SELECT EMPNO,ENAME,SAL INTO ENO2,NM2,SL2 FROM EMP WHERE EMPNO='&Y';

IF(SL1>SL2) THEN
R:=1;
ELSIF(SL1=SL2) THEN
R:=0;
ELSIF(SL1>SL2) THEN
R:=-1;
END IF;

DBMS_OUTPUT.PUT_LINE('RESULT='||R);

EXCEPTION
WHEN NO_DATA_FOUND THEN
DBMS_OUTPUT.PUT_LINE('INVALID EMPNO');

END;
/